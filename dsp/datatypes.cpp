#include<bits/stdc++.h>
//#include<iostream>
using namespace std;

int main()
{
    int a;
    a=12;
    std::cout<<"size of int "<<sizeof(a)<<endl;

    float b;
    std::cout<<"size of float "<<sizeof(b)<<endl;

    double e;
    std::cout<<"size of double "<<sizeof(e)<<endl;

    char c;
    std::cout<<"size of char "<<sizeof(c)<<endl;

    bool d;
    std::cout<<"size of bool "<<sizeof(d)<<endl;

    short int si;
    long int li;
    std::cout<<"size of shortint "<<sizeof(si)<<" bytes"<<endl;
    std::cout<<"size of longint "<<sizeof(li)<<" bytes"<<endl;

    return 0;
}